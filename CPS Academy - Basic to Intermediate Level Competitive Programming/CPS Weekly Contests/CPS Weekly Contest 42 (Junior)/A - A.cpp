#include <bits/stdc++.h>
using namespace std;
#define endl '\n'
#define ll long long

void solve() {
  ll n;
  cin >> n;
  
  if (n >= 2000)
    cout << "YES" << endl;
  else
    cout << "NO" << endl;
}

int main() {
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  ll tt;
  cin >> tt;
  while (tt--) {
    solve();
  }
  return 0;
}
